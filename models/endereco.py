class Endereco:
    def __init__(self, id, rua, numero, bairro, cidade, estado, cep, data_criacao, data_modificacao):
        self.id = id
        self.rua = rua
        self.numero = numero
        self.bairro = bairro
        self.cidade = cidade
        self.estado = estado
        self.cep = cep
        self.data_criacao = data_criacao
        self.data_modificao = data_modificacao